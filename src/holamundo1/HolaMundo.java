package holamundo1;

public class HolaMundo {
    public static void main(String[] args) {
        System.out.println("Hola Mundo");
        System.out.println("Siempre me olvido de Java :v");

        byte entero = 12;
        short sentero = 12456;
        int ientero = 12456;
        long lentero = 12345656;
        float flotante = 12.12f;
        double dflotante = 12.1212;
        char caracter = 'a';
        boolean decision = true;

        System.out.println("Byte entero de 8 bits "+entero);
        System.out.println("Short entero de 16 bits "+ sentero);
        System.out.println("Int entero de 32 bits " + ientero);
        System.out.println("Long entero de 64 bits " + lentero);
        System.out.println("Float flotante con una F al final y de 32 bits " + flotante);
        System.out.println("Double dflotante sin f al final y de 64 bits " + dflotante);
        System.out.println("Char caracter con comillas simples " + caracter);
        System.out.println("Boolean solo puede ser true or false " + decision);
    }
}
